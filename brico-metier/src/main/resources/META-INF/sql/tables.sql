(
	IDUSER BIGINT NOT NULL GENERATED ALWAYS AS IDENTITY,
	DATE_CREATION TIMESTAMP,		
	PSEUDO VARCHAR(100),
	PASSWORD VARCHAR(100),
	TEL VARCHAR(20),
	MAIL VARCHAR(200),
	VILLE VARCHAR(100),
	CP VARCHAR(10),
	RUE VARCHAR(100),
	NB_VOTES BIGINT,
	NOTE BIGINT,
