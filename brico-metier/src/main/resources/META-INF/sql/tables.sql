DROP TABLE KV01.UTILISATEUR;
DROP TABLE KV01.COMMENTAIRE;

CREATE TABLE KV01.UTILISATEUR
(
	IDUSER BIGINT NOT NULL GENERATED ALWAYS AS IDENTITY,
	DATE_CREATION TIMESTAMP,		
	PSEUDO VARCHAR(100),
	PASSWORD VARCHAR(100),
	TEL VARCHAR(20),
	MAIL VARCHAR(200),
	VILLE VARCHAR(100),
	CP VARCHAR(10),
	RUE VARCHAR(100),
	NB_VOTES BIGINT,
	NOTE BIGINT,
	ROLE BIGINT,
	CONSTRAINT USER_PK PRIMARY KEY (IDUSER)
) IN TBS_KV01_D INDEX IN TBS_KV01_X;

CREATE TABLE KV01.COMMENTAIRE
(
	IDCOMM BIGINT NOT NULL GENERATED ALWAYS AS IDENTITY,
	DATE_CREATION TIMESTAMP,
	IDUSER_CREATION BIGINT,
	IDCOMM_REF BIGINT,
	COMM VARCHAR(500),
	NB_VOTES BIGINT,
	NOTE BIGINT,
	COMM_VALIDATE BOOLEAN,
	CONSTRAINT COMM_PK PRIMARY KEY (ICOMM)
) IN TBS_KV01_D INDEX IN TBS_KV01_X;